package programacion3.laboratorio1.Clases.Personas;

public class Persona {
    private String nombre;
    private String ID;

    public Persona(String nombre, String ID) {
        this.nombre = nombre;
        this.ID = ID;
    }

    public String getNombre() {
        return nombre;
    }
    public String getID() {
        return ID;
    }

}
